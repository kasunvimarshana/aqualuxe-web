        $item_output = $args->before;
        
        // Add toggle button for items with children
        if (in_array('menu-item-has-children', $classes)) {
            $submenu_id = 'submenu-' . $args->menu_id . '-' . $depth . '-' . $item->ID;
            
            $item_output .= '<div class="flex justify-between items-center">';
            $item_output .= '<a' . $attributes . '>';
            $item_output .= $args->link_before . $title . $args->link_after;
            $item_output .= '</a>';
            $item_output .= '<button type="button" class="submenu-toggle w-10 h-10 flex items-center justify-center text-dark dark:text-light" @click="activeSubmenu === \'' . $submenu_id . '\' ? activeSubmenu = null : activeSubmenu = \'' . $submenu_id . '\'">';
            $item_output .= '<span x-show="activeSubmenu !== \'' . $submenu_id . '\'">' . aqualuxe_get_svg('chevron-down') . '</span>';
            $item_output .= '<span x-show="activeSubmenu === \'' . $submenu_id . '\'">' . aqualuxe_get_svg('chevron-up') . '</span>';
            $item_output .= '</button>';
            $item_output .= '</div>';
        } else {
            $item_output .= '<a' . $attributes . '>';
            $item_output .= $args->link_before . $title . $args->link_after;
            $item_output .= '</a>';
        }
        
        $item_output .= $args->after;

        /**
         * Filters a menu item's starting output.
         *
         * The menu item's starting output only includes `$args->before`, the opening `<a>`,
         * the menu item's title, the closing `</a>`, and `$args->after`. Currently, there is
