/**
 * Custom walker class for category list.
 */
class AquaLuxe_Category_Walker extends Walker_Category {

	/**
	 * Starts the element output.
	 *
	 * @param string  $output            Used to append additional content (passed by reference).
	 * @param WP_Term $category          Category data object.
	 * @param int     $depth             Depth of category. Used for padding.
	 * @param array   $args              An array of arguments.
	 * @param int     $current_object_id ID of the current category.
	 */
	public function start_el( &$output, $category, $depth = 0, $args = array(), $current_object_id = 0 ) {
		$cat_name = apply_filters( 'list_cats', esc_attr( $category->name ), $category );

		// Don't generate an element if the category name is empty.
		if ( '' === $cat_name ) {
			return;
		}

		$atts         = array();
		$atts['href'] = get_term_link( $category );

		if ( ! empty( $args['current_category'] ) && $args['current_category'] === $category->term_id ) {
			$atts['aria-current'] = 'page';
		}

		$atts = apply_filters( 'category_list_link_attributes', $atts, $category, $depth, $args, $current_object_id );

		$attributes = '';
		foreach ( $atts as $attr => $value ) {
			if ( is_scalar( $value ) && '' !== $value && false !== $value ) {
				$value       = ( 'href' === $attr ) ? esc_url( $value ) : esc_attr( $value );
				$attributes .= ' ' . $attr . '="' . $value . '"';
			}
		}

		$link = sprintf(
			'<a%s class="text-gray-600 hover:text-primary-600 dark:text-gray-300 dark:hover:text-primary-400 transition-colors duration-200">%s</a>',
			$attributes,
			$cat_name
		);

		if ( ! empty( $args['show_count'] ) ) {
			$link .= ' <span class="count text-gray-500 dark:text-gray-400">(' . number_format_i18n( $category->count ) . ')</span>';
		}

		if ( 'list' === $args['style'] ) {
			$output     .= "\t<li";
			$css_classes = array(
				'cat-item',
				'cat-item-' . $category->term_id,
			);

			if ( ! empty( $args['current_category'] ) ) {
				// 'current_category' can be an array, so we use in_array().
				if ( in_array( $category->term_id, (array) $args['current_category'], true ) ) {
					$css_classes[] = 'current-cat';
				}

				// If this is the parent of the current category.
				if ( $args['current_category_ancestors'] && $args['current_category'] && in_array( $category->term_id, (array) $args['current_category_ancestors'], true ) ) {
					$css_classes[] = 'current-cat-parent';
				}
			}

			/**
			 * Filters the list of CSS classes to include with each category in the list.
			 *
			 * @param array  $css_classes An array of CSS classes to be applied to each list item.
			 * @param object $category    Category data object.
			 * @param int    $depth       Depth of page, used for padding.
			 * @param array  $args        An array of arguments.
			 */
			$css_classes = implode( ' ', apply_filters( 'category_css_class', $css_classes, $category, $depth, $args ) );
			$css_classes = $css_classes ? ' class="' . esc_attr( $css_classes ) . '"' : '';

			$output .= $css_classes;
			$output .= ">$link\n";
		} elseif ( isset( $args['separator'] ) ) {
			$output .= "\t$link" . $args['separator'] . "\n";
		} else {
			$output .= "\t$link<br />\n";
		}
	}
}