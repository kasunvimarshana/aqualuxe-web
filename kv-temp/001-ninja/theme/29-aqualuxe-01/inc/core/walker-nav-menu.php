         * @param stdClass $args  An object of wp_nav_menu() arguments.
         * @param int      $depth Depth of menu item.
         */
        $title = apply_filters( 'nav_menu_item_title', $title, $item, $args, $depth );

        // Add custom classes to the link based on depth
        $link_classes = 'block py-2 px-4 ';
        if ( $depth === 0 ) {
            $link_classes .= 'text-dark-700 dark:text-white hover:bg-gray-100 dark:hover:bg-dark-700';
        } else {
            $link_classes .= 'text-dark-600 dark:text-dark-200 hover:bg-gray-50 dark:hover:bg-dark-750 pl-8';
        }
        
        if ( $item->current ) {
            $link_classes .= ' text-primary-600 dark:text-primary-400 bg-gray-50 dark:bg-dark-750';
        }

        $item_output  = $args->before;
        $item_output .= '<a' . $attributes . ' class="' . $link_classes . '">';
        $item_output .= $args->link_before . $title . $args->link_after;
        $item_output .= '</a>';
        $item_output .= $args->after;

